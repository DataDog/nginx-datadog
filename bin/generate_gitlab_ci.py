#!/usr/bin/env -S uv run --script
# /// script
# requires-python = ">=3.9"
# dependencies = ["pyyaml"]
# ///
"""Generate .gitlab/build-and-test-all.yml and .gitlab/build-and-test-fast.yml
from supported_versions.yml using dataclasses + yaml.dump rendering.

Usage:
    uv run bin/generate_gitlab_ci.py           # write files
    uv run bin/generate_gitlab_ci.py --check   # compare and exit 1 on diff
"""

from __future__ import annotations

import argparse
import difflib
import os
import sys
from dataclasses import dataclass

import yaml

REPO_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
VERSIONS_FILE = os.path.join(REPO_ROOT, "supported_versions.yml")
ALL_FILE = os.path.join(REPO_ROOT, ".gitlab", "build-and-test-all.yml")
FAST_FILE = os.path.join(REPO_ROOT, ".gitlab", "build-and-test-fast.yml")

GENERATED_HEADER = (
    "# This file is generated by bin/generate_gitlab_ci.py"
    " from supported_versions.yml.\n"
    "# Do not edit manually. Run: uv run bin/generate_gitlab_ci.py\n")

JOBS_PER_NGINX_VERSION = 8  # 2 arch × 2 base_image × 2 WAF
JOBS_PER_EXTRA_IMAGE = 4  # 2 arch × 1 base_image × 2 WAF
GITLAB_MATRIX_LIMIT = 200

WAF_BOTH = ("ON", "OFF")
WAF_OFF = ("OFF", )

# ---------------------------------------------------------------------------
# YAML custom types and dumper
# ---------------------------------------------------------------------------


class FlowList(list):
    """List rendered in YAML flow style."""


class QuotedStr(str):
    """String rendered with double-quote style."""


def Q(s):
    """Wrap a string as QuotedStr."""
    return QuotedStr(s)


def F(*items):
    """Create a FlowList of QuotedStr items."""
    return FlowList(QuotedStr(item) for item in items)


class GitLabDumper(yaml.Dumper):
    """YAML dumper that indents sequences under their parent key."""

    def increase_indent(self, flow=False, indentless=False):
        return super().increase_indent(flow, indentless=False)


def _represent_flow_list(dumper, data):
    return dumper.represent_sequence(
        'tag:yaml.org,2002:seq', data, flow_style=True)


def _represent_quoted_str(dumper, data):
    return dumper.represent_scalar('tag:yaml.org,2002:str', data, style='"')


GitLabDumper.add_representer(FlowList, _represent_flow_list)
GitLabDumper.add_representer(QuotedStr, _represent_quoted_str)


# ---------------------------------------------------------------------------
# Matrix row builders (return dicts with F() values)
# ---------------------------------------------------------------------------


def nginx_test(version: str,
               alpine: bool = True,
               waf: tuple[str, ...] | None = WAF_BOTH) -> dict:
    """One matrix row for an nginx version."""
    images = [f"nginx:{version}"]
    if alpine:
        images.append(f"nginx:{version}-alpine")
    row = {
        "ARCH": F("amd64", "arm64"),
        "BASE_IMAGE": F(*images),
        "NGINX_VERSION": F(version),
    }
    if waf:
        row["WAF"] = F(*waf)
    return row


def extra_image_test(image: dict) -> dict:
    """One matrix row for an extra test image (e.g. amazonlinux)."""
    return {
        "ARCH": F("amd64", "arm64"),
        "BASE_IMAGE": F(image["base_image"]),
        "NGINX_VERSION": F(image["nginx_version"]),
        "WAF": F(*WAF_BOTH),
    }


# ---------------------------------------------------------------------------
# Job dataclasses — each render() returns a dict
# ---------------------------------------------------------------------------


@dataclass
class Build:
    """Build job: ARCH × versions [× WAF] matrix."""
    name: str
    extends: list[str]
    var_name: str
    versions: list[str]
    waf: tuple[str, ...] | None = WAF_BOTH
    def render(self) -> dict:
        row = {"ARCH": F("amd64", "arm64")}
        row[self.var_name] = [Q(v) for v in self.versions]
        if self.waf:
            row["WAF"] = F(*self.waf)
        return {
            self.name: {
                "extends": list(self.extends),
                "parallel": {"matrix": [row]},
            }
        }


@dataclass
class Test:
    """Test job: either a version-list matrix or explicit per-version rows."""
    name: str
    extends: list[str]
    needs: str | None = None
    var_name: str | None = None
    versions: list[str] | None = None
    rows: list[dict] | None = None
    waf: tuple[str, ...] | None = WAF_BOTH

    def render(self) -> dict:
        body: dict = {"extends": list(self.extends)}
        if self.needs:
            body["needs"] = FlowList([self.needs])
        if self.rows is not None:
            matrix = self.rows
        else:
            row = {"ARCH": F("amd64", "arm64")}
            row[self.var_name] = [Q(v) for v in self.versions]
            if self.waf:
                row["WAF"] = F(*self.waf)
            matrix = [row]
        body["parallel"] = {"matrix": matrix}
        return {self.name: body}


# ---------------------------------------------------------------------------
# Version extraction
# ---------------------------------------------------------------------------


def _sort_key(v):
    return tuple(int(p) for p in v.split("."))


def _versions(entries, flag=None):
    if flag:
        entries = [e for e in entries if e.get(flag)]
    return sorted((e["version"] for e in entries), key=_sort_key)


def _extras(data):
    return sorted(
        data["nginx"].get("extra_test_images", []),
        key=lambda e: _sort_key(e["nginx_version"]),
    )


def split_nginx_test_versions(all_versions, extras):
    """Split versions across test-nginx-all / test-nginx-all-bis
    to stay under the GitLab 200-job matrix limit."""
    job_count = 0
    split_at = len(all_versions)
    for i, _ in enumerate(all_versions):
        job_count += JOBS_PER_NGINX_VERSION
        if job_count > GITLAB_MATRIX_LIMIT:
            split_at = i
            break

    first = all_versions[:split_at]
    second = all_versions[split_at:]

    extra_jobs = len(extras) * JOBS_PER_EXTRA_IMAGE
    if len(first) * JOBS_PER_NGINX_VERSION + extra_jobs <= GITLAB_MATRIX_LIMIT:
        return first, extras, second, []
    return first, [], second, extras


# ---------------------------------------------------------------------------
# File generators
# ---------------------------------------------------------------------------


def render_jobs(jobs: list) -> str:
    blocks = []
    for job in jobs:
        text = yaml.dump(job.render(), Dumper=GitLabDumper,
                         default_flow_style=False, sort_keys=False, width=1000)
        blocks.append(text.rstrip('\n'))
    return GENERATED_HEADER + "\n" + "\n\n".join(blocks) + "\n"


def generate_all(data):
    nginx = _versions(data["nginx"]["versions"])
    rum = _versions(data["nginx"]["versions"], "rum")
    ingress = _versions(data["ingress_nginx"]["versions"])
    openresty = _versions(data["openresty"]["versions"])
    extras = _extras(data)

    first_v, first_ex, second_v, second_ex = split_nginx_test_versions(
        nginx, extras)

    return render_jobs([
        Build(name="build-nginx-all",
              extends=[".build-all", ".build-nginx"],
              var_name="NGINX_VERSION",
              versions=nginx),
        Build(name="build-nginx-rum-all",
              extends=[".build-all", ".build-nginx-rum"],
              var_name="NGINX_VERSION",
              versions=rum,
              waf=WAF_OFF),
        Build(name="build-ingress-nginx-all",
              extends=[".build-all", ".build-ingress-nginx"],
              var_name="INGRESS_NGINX_VERSION",
              versions=ingress,
              waf=None),
        Build(name="build-openresty-all",
              extends=[".build-all", ".build-openresty"],
              var_name="RESTY_VERSION",
              versions=openresty),
        Test(name="test-nginx-all",
             extends=[".test-all", ".test-nginx"],
             rows=[nginx_test(v) for v in first_v] +
             [extra_image_test(img) for img in first_ex]),
        Test(name="test-nginx-all-bis",
             extends=[".test-all", ".test-nginx"],
             rows=[nginx_test(v) for v in second_v] +
             [extra_image_test(img) for img in second_ex]),
        Test(name="test-nginx-rum-all",
             extends=[".test-all", ".test-nginx-rum"],
             rows=[nginx_test(v, alpine=False, waf=WAF_OFF) for v in rum]),
        Test(name="test-ingress-nginx-all",
             extends=[".test-all", ".test-ingress-nginx"],
             var_name="INGRESS_NGINX_VERSION",
             versions=ingress,
             waf=None),
        Test(name="test-openresty-all",
             extends=[".test-all", ".test-openresty"],
             var_name="RESTY_VERSION",
             versions=openresty),
    ])


def generate_fast(data):
    fast_nginx = _versions(data["nginx"]["versions"], "fast")
    rum = _versions(data["nginx"]["versions"], "rum")
    fast_ingress = _versions(data["ingress_nginx"]["versions"], "fast")
    fast_openresty = _versions(data["openresty"]["versions"], "fast")
    extras = _extras(data)

    return render_jobs([
        Build(name="build-nginx-fast",
              extends=[".build-and-test-fast", ".build-nginx"],
              var_name="NGINX_VERSION",
              versions=fast_nginx),
        Build(name="build-ingress-nginx-fast",
              extends=[".build-and-test-fast", ".build-ingress-nginx"],
              var_name="INGRESS_NGINX_VERSION",
              versions=fast_ingress,
              waf=None),
        Build(name="build-openresty-fast",
              extends=[".build-and-test-fast", ".build-openresty"],
              var_name="RESTY_VERSION",
              versions=fast_openresty),
        Build(name="build-nginx-rum-fast",
              extends=[".build-and-test-fast", ".build-nginx-rum"],
              var_name="NGINX_VERSION",
              versions=rum,
              waf=WAF_OFF),
        Test(name="test-nginx-fast",
             extends=[".build-and-test-fast", ".test-nginx"],
             rows=[nginx_test(v) for v in fast_nginx] +
             [extra_image_test(img) for img in extras],
             needs="build-nginx-fast"),
        Test(name="test-ingress-nginx-fast",
             extends=[".build-and-test-fast", ".test-ingress-nginx"],
             var_name="INGRESS_NGINX_VERSION",
             versions=fast_ingress,
             waf=None,
             needs="build-ingress-nginx-fast"),
        Test(name="test-openresty-fast",
             extends=[".build-and-test-fast", ".test-openresty"],
             var_name="RESTY_VERSION",
             versions=fast_openresty,
             needs="build-openresty-fast"),
        Test(name="test-nginx-rum-fast",
             extends=[".build-and-test-fast", ".test-nginx-rum"],
             rows=[nginx_test(v, alpine=False, waf=WAF_OFF) for v in rum],
             needs="build-nginx-rum-fast"),
    ])


# ---------------------------------------------------------------------------
# Check / write
# ---------------------------------------------------------------------------


def check_or_write(generated, path, check_mode):
    if check_mode:
        try:
            with open(path) as f:
                existing = f.read()
        except FileNotFoundError:
            print(f"MISSING: {path}")
            return True
        if existing != generated:
            sys.stdout.writelines(
                difflib.unified_diff(
                    existing.splitlines(keepends=True),
                    generated.splitlines(keepends=True),
                    fromfile=f"{path} (on disk)",
                    tofile=f"{path} (generated)",
                ))
            return True
        return False
    else:
        with open(path, "w") as f:
            f.write(generated)
        print(f"Wrote {path}")
        return False


def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--check",
                        action="store_true",
                        help="Exit 1 if generated files differ from on-disk")
    args = parser.parse_args()

    with open(VERSIONS_FILE) as f:
        data = yaml.safe_load(f)

    any_diff = False
    any_diff |= check_or_write(generate_all(data), ALL_FILE, args.check)
    any_diff |= check_or_write(generate_fast(data), FAST_FILE, args.check)

    if args.check:
        if any_diff:
            print("\nGenerated CI files are out of date. "
                  "Run: uv run bin/generate_gitlab_ci.py")
            sys.exit(1)
        else:
            print("Generated CI files are up to date.")


if __name__ == "__main__":
    main()
