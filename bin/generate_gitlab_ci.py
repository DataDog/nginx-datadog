#!/usr/bin/env python3
# /// script
# requires-python = ">=3.9"
# dependencies = ["ruamel.yaml"]
# ///
"""Generate .gitlab/build-and-test-all.yml and .gitlab/build-and-test-fast.yml
from supported_versions.yml.

Usage:
    uv run bin/generate_gitlab_ci.py           # write files
    uv run bin/generate_gitlab_ci.py --check   # compare and exit 1 on diff
"""

import argparse
import difflib
import os
import sys
from io import StringIO

from ruamel.yaml import YAML
from ruamel.yaml.comments import CommentedMap, CommentedSeq
from ruamel.yaml.scalarstring import DoubleQuotedScalarString as DQ

REPO_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
VERSIONS_FILE = os.path.join(REPO_ROOT, "supported_versions.yml")
ALL_FILE = os.path.join(REPO_ROOT, ".gitlab", "build-and-test-all.yml")
FAST_FILE = os.path.join(REPO_ROOT, ".gitlab", "build-and-test-fast.yml")

GENERATED_HEADER = (
    "# This file is generated by bin/generate_gitlab_ci.py"
    " from supported_versions.yml.\n"
    "# Do not edit manually. Run: uv run bin/generate_gitlab_ci.py\n\n"
)

# Each nginx test matrix entry = 8 jobs: 2 arch × 2 base_image × 2 WAF.
# Extra test images = 4 jobs each: 2 arch × 1 base_image × 2 WAF.
JOBS_PER_NGINX_VERSION = 8
JOBS_PER_EXTRA_IMAGE = 4
GITLAB_MATRIX_LIMIT = 200

_reader = YAML(typ="safe")
_writer = YAML()
_writer.indent(mapping=2, sequence=4, offset=2)


# ---------------------------------------------------------------------------
# YAML helpers
# ---------------------------------------------------------------------------

def _flow(*items):
    """Flow-style sequence with double-quoted items: ["amd64", "arm64"]."""
    s = CommentedSeq([DQ(i) for i in items])
    s.fa.set_flow_style()
    return s


def _flow_plain(*items):
    """Flow-style sequence with plain items: [build-nginx-fast]."""
    s = CommentedSeq(list(items))
    s.fa.set_flow_style()
    return s


def _qversions(versions):
    """Block-style list of double-quoted version strings."""
    return [DQ(v) for v in versions]


def _dump(data):
    buf = StringIO()
    _writer.dump(data, buf)
    # Add blank lines between top-level jobs for readability.
    import re
    return re.sub(r'\n([a-z#])', r'\n\n\1', buf.getvalue())


# ---------------------------------------------------------------------------
# Version extraction (sorted for stable output)
# ---------------------------------------------------------------------------

def _sort_key(v):
    return tuple(int(p) for p in v.split("."))


def _versions(entries, flag=None):
    if flag:
        entries = [e for e in entries if e.get(flag)]
    return sorted((e["version"] for e in entries), key=_sort_key)


def _extras(data):
    return sorted(
        data["nginx"].get("extra_test_images", []),
        key=lambda e: _sort_key(e["nginx_version"]),
    )


def load_versions():
    with open(VERSIONS_FILE) as f:
        return _reader.load(f)


# ---------------------------------------------------------------------------
# Job builders
# ---------------------------------------------------------------------------

def _matrix_entry(var_name, versions, waf=True):
    """Single matrix entry with ARCH × versions [× WAF]."""
    e = CommentedMap()
    e["ARCH"] = _flow("amd64", "arm64")
    e[var_name] = _qversions(versions)
    if waf is True:
        e["WAF"] = _flow("ON", "OFF")
    elif waf:
        e["WAF"] = _flow(waf)
    return e


def build_job(extends, var_name, versions, waf=True):
    return CommentedMap({
        "extends": extends,
        "parallel": CommentedMap({
            "matrix": [_matrix_entry(var_name, versions, waf)]
        }),
    })


def test_job(extends, var_name, versions, waf=True, needs=None):
    j = CommentedMap()
    j["extends"] = extends
    if needs:
        j["needs"] = _flow_plain(needs)
    j["parallel"] = CommentedMap({
        "matrix": [_matrix_entry(var_name, versions, waf)]
    })
    return j


def nginx_test_job(extends, versions, extras=None, needs=None):
    """Test job with per-version BASE_IMAGE entries (nginx + alpine)."""
    entries = []
    for v in versions:
        e = CommentedMap()
        e["ARCH"] = _flow("amd64", "arm64")
        e["BASE_IMAGE"] = _flow(f"nginx:{v}", f"nginx:{v}-alpine")
        e["NGINX_VERSION"] = _flow(v)
        e["WAF"] = _flow("ON", "OFF")
        entries.append(e)
    for img in (extras or []):
        e = CommentedMap()
        e["ARCH"] = _flow("amd64", "arm64")
        e["BASE_IMAGE"] = _flow(img["base_image"])
        e["NGINX_VERSION"] = _flow(img["nginx_version"])
        e["WAF"] = _flow("ON", "OFF")
        entries.append(e)
    j = CommentedMap()
    j["extends"] = extends
    if needs:
        j["needs"] = _flow_plain(needs)
    j["parallel"] = CommentedMap({"matrix": entries})
    return j


def rum_test_job(extends, versions, needs=None):
    """Test job for RUM: WAF OFF only, no alpine."""
    entries = []
    for v in versions:
        e = CommentedMap()
        e["ARCH"] = _flow("amd64", "arm64")
        e["BASE_IMAGE"] = _flow(f"nginx:{v}")
        e["NGINX_VERSION"] = _flow(v)
        e["WAF"] = _flow("OFF")
        entries.append(e)
    j = CommentedMap()
    j["extends"] = extends
    if needs:
        j["needs"] = _flow_plain(needs)
    j["parallel"] = CommentedMap({"matrix": entries})
    return j


# ---------------------------------------------------------------------------
# Split logic for the 200-job GitLab matrix limit
# ---------------------------------------------------------------------------

def split_nginx_test_versions(all_versions, extras):
    """Split versions across test-nginx-all / test-nginx-all-bis."""
    job_count = 0
    split_at = len(all_versions)
    for i, _ in enumerate(all_versions):
        job_count += JOBS_PER_NGINX_VERSION
        if job_count > GITLAB_MATRIX_LIMIT:
            split_at = i
            break

    first = all_versions[:split_at]
    second = all_versions[split_at:]

    extra_jobs = len(extras) * JOBS_PER_EXTRA_IMAGE
    if len(first) * JOBS_PER_NGINX_VERSION + extra_jobs <= GITLAB_MATRIX_LIMIT:
        return first, extras, second, []
    return first, [], second, extras


# ---------------------------------------------------------------------------
# File generators
# ---------------------------------------------------------------------------

def generate_all(data):
    nginx = _versions(data["nginx"]["versions"])
    rum = _versions(data["nginx"]["versions"], "rum")
    ingress = _versions(data["ingress_nginx"]["versions"])
    openresty = _versions(data["openresty"]["versions"])
    extras = _extras(data)

    first_v, first_ex, second_v, second_ex = split_nginx_test_versions(
        nginx, extras
    )

    jobs = CommentedMap()
    jobs["build-nginx-all"] = build_job(
        [".build-all", ".build-nginx"], "NGINX_VERSION", nginx)
    jobs["build-nginx-rum-all"] = build_job(
        [".build-all", ".build-nginx-rum"], "NGINX_VERSION", rum, waf="OFF")
    jobs["build-ingress-nginx-all"] = build_job(
        [".build-all", ".build-ingress-nginx"],
        "INGRESS_NGINX_VERSION", ingress, waf=False)
    jobs["build-openresty-all"] = build_job(
        [".build-all", ".build-openresty"], "RESTY_VERSION", openresty)
    jobs["test-nginx-all"] = nginx_test_job(
        [".test-all", ".test-nginx"], first_v, first_ex)
    jobs.yaml_set_comment_before_after_key(
        "test-nginx-all-bis",
        before="GitLab is not able to handle a matrix with more than 200 jobs!")
    jobs["test-nginx-all-bis"] = nginx_test_job(
        [".test-all", ".test-nginx"], second_v, second_ex)
    jobs["test-nginx-rum-all"] = rum_test_job(
        [".test-all", ".test-nginx-rum"], rum)
    jobs["test-ingress-nginx-all"] = test_job(
        [".test-all", ".test-ingress-nginx"],
        "INGRESS_NGINX_VERSION", ingress, waf=False)
    jobs["test-openresty-all"] = test_job(
        [".test-all", ".test-openresty"], "RESTY_VERSION", openresty)

    return GENERATED_HEADER + _dump(jobs)


def generate_fast(data):
    fast_nginx = _versions(data["nginx"]["versions"], "fast")
    rum = _versions(data["nginx"]["versions"], "rum")
    fast_ingress = _versions(data["ingress_nginx"]["versions"], "fast")
    fast_openresty = _versions(data["openresty"]["versions"], "fast")
    extras = _extras(data)
    cov = data["nginx"]["coverage_version"]

    # build-nginx-fast needs a comment on NGINX_VERSION
    nginx_entry = _matrix_entry("NGINX_VERSION", fast_nginx)
    nginx_entry.yaml_set_comment_before_after_key(
        "NGINX_VERSION",
        before="The version used by system-tests must be one of the ones"
        " below. See https://github.com/DataDog/system-tests/pull/6113.",
        indent=8)
    build_nginx_fast = CommentedMap({
        "extends": [".build-and-test-fast", ".build-nginx"],
        "parallel": CommentedMap({"matrix": [nginx_entry]}),
    })

    jobs = CommentedMap()
    jobs["build-nginx-fast"] = build_nginx_fast
    jobs["build-ingress-nginx-fast"] = build_job(
        [".build-and-test-fast", ".build-ingress-nginx"],
        "INGRESS_NGINX_VERSION", fast_ingress, waf=False)
    jobs["build-openresty-fast"] = build_job(
        [".build-and-test-fast", ".build-openresty"],
        "RESTY_VERSION", fast_openresty)
    jobs["build-nginx-rum-fast"] = build_job(
        [".build-and-test-fast", ".build-nginx-rum"],
        "NGINX_VERSION", rum, waf="OFF")
    jobs["test-nginx-fast"] = nginx_test_job(
        [".build-and-test-fast", ".test-nginx"],
        fast_nginx, extras, needs="build-nginx-fast")
    jobs["test-ingress-nginx-fast"] = test_job(
        [".build-and-test-fast", ".test-ingress-nginx"],
        "INGRESS_NGINX_VERSION", fast_ingress, waf=False,
        needs="build-ingress-nginx-fast")
    jobs["test-openresty-fast"] = test_job(
        [".build-and-test-fast", ".test-openresty"],
        "RESTY_VERSION", fast_openresty,
        needs="build-openresty-fast")
    jobs["test-nginx-rum-fast"] = rum_test_job(
        [".build-and-test-fast", ".test-nginx-rum"],
        rum, needs="build-nginx-rum-fast")
    jobs["coverage"] = CommentedMap({
        "extends": [".build-and-test-fast", ".build"],
        "variables": CommentedMap({
            "ARCH": DQ("amd64"),
            "BASE_IMAGE": DQ(f"nginx:{cov}"),
            "NGINX_VERSION": DQ(cov),
            "WAF": DQ("ON"),
        }),
        "tags": _flow_plain(f"docker-in-docker:$ARCH"),
        "script": ["make coverage"],
    })

    return GENERATED_HEADER + _dump(jobs)


# ---------------------------------------------------------------------------
# Check / write
# ---------------------------------------------------------------------------

def check_or_write(generated, path, check_mode):
    if check_mode:
        try:
            with open(path) as f:
                existing = f.read()
        except FileNotFoundError:
            print(f"MISSING: {path}")
            return True
        if existing != generated:
            sys.stdout.writelines(difflib.unified_diff(
                existing.splitlines(keepends=True),
                generated.splitlines(keepends=True),
                fromfile=f"{path} (on disk)",
                tofile=f"{path} (generated)",
            ))
            return True
        return False
    else:
        with open(path, "w") as f:
            f.write(generated)
        print(f"Wrote {path}")
        return False


def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--check", action="store_true",
                        help="Exit 1 if generated files differ from on-disk")
    args = parser.parse_args()

    data = load_versions()
    any_diff = False
    any_diff |= check_or_write(generate_all(data), ALL_FILE, args.check)
    any_diff |= check_or_write(generate_fast(data), FAST_FILE, args.check)

    if args.check:
        if any_diff:
            print("\nGenerated CI files are out of date. "
                  "Run: uv run bin/generate_gitlab_ci.py")
            sys.exit(1)
        else:
            print("Generated CI files are up to date.")


if __name__ == "__main__":
    main()
